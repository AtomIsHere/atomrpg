package com.github.atomishere.atomrpg.attributes.player;

import com.google.inject.Inject;
import com.google.inject.Singleton;
import org.bukkit.event.EventHandler;
import org.bukkit.event.Listener;
import org.bukkit.event.player.PlayerJoinEvent;
import org.bukkit.event.player.PlayerQuitEvent;

@Singleton
public class PlayerAttributeListener implements Listener {
    @Inject
    private PlayerAttributeManager attributeManager;

    @EventHandler
    public void onPlayerJoin(PlayerJoinEvent event) {
        attributeManager.registerPlayer(event.getPlayer());
    }

    @EventHandler
    public void onPlayerQuit(PlayerQuitEvent event) {
        attributeManager.cleanPlayer(event.getPlayer());
    }
}
